package FlowControl;

public class SwitchStatement {

    public static void main(String[] args) {

        String food = "Sandwich";

        switch(food){
            case "Burger":
                System.out.println("Please serve Burger");
                break;
            case "IceCream":
                System.out.println("Please server Ice Cream");
                break;
            case "Noodles":
                System.out.println("Please serve Noodles");
                break;
            case"Pizza":
                System.out.println("Please serve Pizza");
                break;
            default:
                System.out.println("Sorry we only server Burger,Noodles,Ice Cream, Pizza");





    }
}}