package ConceOfOops1;

public class CoceptofOverriding {

    public static void main(String[] args) {

    }
    public static void login(){

        System.out.println("zero parameter");
    }
    
    public static void login(int a){

        System.out.println("one parameter");
    }

    public static void login(int a , int b){

        System.out.println("two parameter");
    }
    
    public static void login(boolean b){
        System.out.println("one parameter with different data type");

    }


}
