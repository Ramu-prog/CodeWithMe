package ConceOfOops1;

public class overriding1 {

    public void Add(int a , int b){

        int sum;
        sum= a+b;
        System.out.println("some of given value :  " +sum);
    }

    public void Add(int a , int b , int c){

        int sum;
        sum= a+b+c;
        System.out.println("some of given value :  " +sum);
    }

    public void Add(int a , int b ,int c ,int d){

        int sum;
        sum= a+b+c+d;
        System.out.println("some of given value :  " +sum);
    }


}
